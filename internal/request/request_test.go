func Test_Unsuported_HTTP_Version_Failure(t *testing.T) {
	_, err := RequestFromReader(&chunkReader{
		data:            "GET /coffee HTTP/2.1\r\nHost: localhost:42069\r\nUser-Agent: curl/7.81.0\r\nAccept: */*\r\n\r\n",
		numBytesPerRead: 3,
	})
	require.Error(t, err)
}

